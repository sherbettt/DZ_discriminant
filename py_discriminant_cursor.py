# quadratic_equation.py

def discriminant(a: float, b: float, c: float) -> float:
    """
    Вычисляет дискриминант квадратного уравнения ax² + bx + c = 0
    """
    return b**2 - 4*a*c

def solve(a: float, b: float, c: float) -> None:
    """
    Находит и выводит корни квадратного уравнения ax² + bx + c = 0
    """
    if a == 0:
        print("Ошибка: коэффициент a не может быть равен нулю")
        return
    
    D = discriminant(a, b, c)
    
    if D < 0:
        print("Уравнение не имеет действительных корней")
        return
    
    x1 = (-b + D**0.5) / (2*a)
    x2 = (-b - D**0.5) / (2*a)
    
    if D == 0:
        print(f"Уравнение имеет один корень: x = {x1}")
    else:
        print(f"Уравнение имеет два корня: x1 = {x1}, x2 = {x2}")

# Примеры использования
if __name__ == "__main__":
    # Пример 1: x² + 5x + 6 = 0
    print("Пример 1: x² + 5x + 6 = 0")
    solve(1, 5, 6)
    
    # Пример 2: x² + 2x + 1 = 0
    print("\nПример 2: x² + 2x + 1 = 0")
    solve(1, 2, 1)
    
    # Пример 3: x² + 1 = 0
    print("\nПример 3: x² + 1 = 0")
    solve(1, 0, 1) 

